#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;
	int	s_lenght;

	i = 0;
	s_lenght = ft_strlen(s);
	while (i < s_lenght)
	{
		if (s[i] == (unsigned char)c)
			return ((char *)&s[i]);
		i++;
	}
	if ((unsigned char)c == '\0')
		return ((char *)&s[i]);
	return (0);
}
